public class interface11 {
    interface Try{
        int a=10;
        public int b=70;
        public static final int c=-10;
        final int e=100;
        static int f=1006;
    }
    public static void main(String[] args) {
        System.out.println(Try.a);
        System.out.println(Try.b);
        System.out.println(Try.c);
        System.out.println(Try.e);
        System.out.println(Try.f);
    }
}
